/**
 * 題目：343. Integer Break
 * 難度：中等 (Medium)
 * 描述：給定一個整數 n，將其拆分為 k 個正整數之和 (k >= 2)，使得這些整數的乘積最大。
 * 
 * 解法思路 (數學貪婪法)：
 * 1. 若 n <= 3，直接回傳 n - 1 (例如 n=2 為 1*1=1, n=3 為 2*1=2)。
 * 2. 對於 n > 3，儘量拆出 3。因為 3 是小於 5 的數中，乘積成長率最高的數。
 * 3. 當剩餘數字為 4 時，保留 4 (即 2 * 2) 比拆成 3 * 1 更大。
 * 4. 循環將 n 減去 3 並累乘，最後乘上剩下的數字。
 */

class Solution {
public:
    int integerBreak(int n) {
        // 基本邊界情況
        if (n <= 3) return n - 1;
        
        int product = 1;
        // 儘量將 n 拆成 3 的倍數
        while (n > 4) {
            product *= 3;
            n -= 3;
        }
        
        // 最後剩餘的 n 必定是 2, 3 或 4
        return product * n;
    }
};
