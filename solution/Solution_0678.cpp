/**
 * 題目：678. Valid Parenthesis String (有效的括號字串)
 * 難度：中等 (Medium)
 * 描述：檢查包含 '(', ')', '*' 的字串是否有效，'*' 可代表空、'(', 或 ')'。
 * * 時間複雜度：O(N) - 僅遍歷一次字串。
 * 空間複雜度：O(1) - 僅使用兩個整數變數追蹤狀態。
 * * 優化思路：
 * 1. 區間追蹤：維護 `low` (最少需要匹配的左括號數) 與 `high` (最多能匹配的左括號數)。
 * 2. 邊界檢查：若 `high < 0`，代表右括號過多，立即返回 false。
 * 3. 負值修正：`low` 不能小於 0，因為左括號數量不可能為負。
 */

class Solution {
public:
    bool checkValidString(string s) {
        int low = 0;  // 最少剩餘的左括號數量 (將 '*' 視為 ')' 時)
        int high = 0; // 最多剩餘的左括號數量 (將 '*' 視為 '(' 時)

        for (const char c : s) {
            // 更新 high：將 '*' 視為 '('，')' 則消耗 '('
            high += (c == ')') ? -1 : 1;
            
            // 更新 low：將 '*' 視為 ')'，'(' 則增加 '('
            low += (c == '(') ? 1 : -1;

            // 如果 high < 0，代表即便把所有 '*' 都當成 '('，左括號依然不足以抵銷右括號
            if (high < 0) return false;

            // low 不能為負，若低於 0 代表 '*' 被用作左括號抵銷了，重置為 0
            if (low < 0) low = 0;
        }

        // 若最後 low 為 0，代表存在一種可能性使得左括號完全匹配
        return (low == 0);
    }
};
