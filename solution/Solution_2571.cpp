/**
 * 題目：2571. Minimum Operations to Reduce an Integer to 0
 * 描述：給你一個正整數 n。你可以進行以下操作任意次：
 *       選擇一個整數 i（滿足 0 <= i），並將 n 加上或減去 2^i。
 *       返回將 n 減到 0 所需的最少操作次數。
 * 
 * 解法思路：
 * 1. 貪心與二進位規律：
 *    - 如果當前數字的二進位結尾是 `11`（即 `(n & 3) == 3`），代表有連續的兩個 1。
 *      這時與其分開減掉兩次 1，不如透過「加 1」讓進位吃掉這些連續的 1，這樣操作次數更少（合併連續 1）。
 *    - 如果結尾是單獨的 `1`（即奇數但不是 3），直接將其減 1（`n -= 1`）。
 *    - 如果結尾是 `0`（偶數），代表該位元不需要處理，直接將數字向右位移（`n >>= 1`）跳過。
 * 2. 狀態機模擬：
 *    - 持續迴圈直到 `n == 0`，並累計操作次數 `ans`。
 */

class Solution {
public:
    int minOperations(int n) {
        int ans = 0;
        while (n > 0) {
            // 情況一：結尾是 '11' (二進位末兩位為 3)，選擇加 1 以合併連續的 1
            if ((n & 3) == 3) {
                n += 1;
                ans += 1;
            } 
            // 情況二：結尾是單獨的 '1' (奇數)，選擇減 1 消除該 bit
            else if (n % 2 == 1) { // 也可以寫成 (n & 1)
                n -= 1;
                ans += 1;
            } 
            // 情況三：結尾是 '0' (偶數)，直接右移跳過
            else {
                n >>= 1;
            }
        }
        return ans;
    }
};
