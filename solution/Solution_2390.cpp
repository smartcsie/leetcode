/**
 * 題目：2390. Removing Stars From a String (從字串中移除星號)
 * 難度：中等 (Medium)
 * 描述：給你一個字串 s，其中包含一些星號 '*'。在一步操作中，你可以：
 *      選擇字串中的一個星號，並將其與它左側最近的非星號字元一同刪除。
 *      返回刪除所有星號後的字串。
 * 
 * 時間複雜度：O(n) - 遍歷整個字串一次，每個字元最多進出「堆疊」一次。
 * 空間複雜度：O(n) - 最壞情況下（沒有星號），結果字串將存儲所有輸入字元。
 * 
 * 解法思路：
 * 1. 本題的關鍵在於：星號 '*' 會消除它「左側最近」的一個字元。
 * 2. 這是一個典型的「後進先出 (LIFO)」問題，使用 Stack (堆疊) 最為直觀。
 * 3. 實作上，我們直接利用一個字串 `res` 作為堆疊：
 *    - 若當前字元是 '*'：表示發生了消除事件，執行 `res.pop_back()` 將最近放入的字元移除。
 *    - 若當前字元不是 '*'：則將其壓入 `res`。
 * 4. 題目保證星號操作必定有效，因此不需要擔心空堆疊彈出的異常情況（但加上檢查更穩健）。
 */

class Solution {
public:
    string removeStars(string s) {
        string res = "";
        for(char c : s) {
            if(c == '*' && !res.empty()) res.pop_back();
            else if(c != '*') res.push_back(c);
        }
        return res;
    }
};
