/**
 * 題目：3114. Latest Time You Can Obtain After Replacing Characters
 * 描述：給定一個字串表示 12 小時制的時間（格式為 "HH:MM"），其中某些數字可能是 '?'。
 *       請將 '?' 替換成數字，求出能表示的最晚有效時間。
 * 
 * 解法思路 (Greedy)：
 * - 12 小時制的有效範圍是 "00:00" 到 "11:59"。
 * - 第 0 位 (時十位)：如果第 1 位已經是 '2'~'9'，則第 0 位只能是 '0' (如 09)；否則最大可以是 '1' (如 11)。
 * - 第 1 位 (時個位)：如果第 0 位是 '1'，則最大只能是 '1' (即 11)；否則可以是 '9' (即 09)。
 * - 第 3 位 (分十位)：最大為 '5' (即 59分)。
 * - 第 4 位 (分個位)：最大為 '9'。
 */

class Solution {
public:
    std::string findLatestTime(std::string s) {
        for (int i = 0; i < s.size(); i++) {
            if (i == 0 && s[i] == '?') {
                // 如果第二個數字是 2~9，第一個數字只能是 0
                if (s[1] >= '2' && s[1] <= '9') s[0] = '0';
                else s[0] = '1';
            } else if (i == 1 && s[i] == '?') {
                // 如果第一個數字是 1，第二個數字最大是 1 (11點)
                if (s[0] == '1') s[1] = '1';
                else s[1] = '9';
            } else if (i == 3 && s[i] == '?') {
                // 分鐘的十位數最大為 5
                s[3] = '5';
            } else if (i == 4 && s[i] == '?') {
                // 分鐘的個位數最大為 9
                s[4] = '9';
            } 
        }
        return s;
    }
};
