/**
 * 題目：2652. Sum Multiples (倍數求和)
 * 難度：簡單 (Easy)
 * 描述：給定一個正整數 n，求出所有滿足「能被 3, 5 或 7 整除」的整數和。
 * 
 * 時間複雜度：O(n) - 遍歷 1 到 n 每個數字一次。
 * 空間複雜度：O(1) - 僅使用一個常數空間儲存總和。
 * 
 * 解法思路：
 * 1. 初始化 sum 為 0。
 * 2. 使用迴圈從 1 遍歷到 n。
 * 3. 檢查當前數字 i 是否滿足 (i % 3 == 0 || i % 5 == 0 || i % 7 == 0)。
 * 4. 若滿足，則將 i 加入 sum。
 */

class Solution {
public:
    int sumOfMultiples(int n) {
        int sum = 0;
        for (int i = 1; i <= n; i++) {
            // 使用邏輯或 (OR) 判斷是否為其中任意一個數的倍數
            if (i % 3 == 0 || i % 5 == 0 || i % 7 == 0) {
                sum += i;
            }
        }
        return sum;
    }
};
