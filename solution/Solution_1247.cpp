/**
 * 題目：1247. Minimum Swaps to Make Strings Equal
 * 描述：給你兩個長度相同的字串 s1 和 s2，只包含字元 'x' 和 'y'。
 *       你可以選擇兩個不同下標 i 和 j，然後交換 s1[i] 和 s2[j]。
 *       求出使 s1 和 s2 相等的最小交換次數。如果無法做到，返回 -1。
 * 
 * 解法思路：
 * 1. 相同位置的字元若已經一樣（s1[i] == s2[i]），則無需理會，直接跳過。
 * 2. 只有不同位置的字元需要處理，總共會產生兩種不匹配的形態：
 *    - `xy`：`s1[i] == 'x'` 且 `s2[i] == 'y'`
 *    - `yx`：`s1[i] == 'y'` 且 `s2[i] == 'x'`
 * 3. 檢查總不匹配數的奇偶性：若 `(xy + yx)` 為奇數，代表無法透過交換達成平衡，直接返回 -1。
 * 4. 貪心配對：
 *    - 兩對 `xy` 可以透過 1 次交換全部修復（例如 "xx" 與 "yy" 交換）。
 *    - 兩對 `yx` 也可以透過 1 次交換全部修復。
 *    - 若各自除以 2 都有剩餘（即 `xy` 和 `yx` 各剩 1 個），則需要額外多花 2 次交換來解決。
 */

class Solution {
public:
    int minimumSwap(std::string s1, std::string s2) {
        int xy = 0; // s1[i] == 'x' 且 s2[i] == 'y' 的數量
        int yx = 0; // s1[i] == 'y' 且 s2[i] == 'x' 的數量
        
        // 1. 統計不匹配的類型
        for (size_t i = 0; i < s1.length(); ++i) {
            if (s1[i] == s2[i]) {
                continue;
            } else if (s1[i] == 'x') {
                ++xy;
            } else {
                ++yx;
            }
        }
        
        // 2. 總和為奇數時無法配對，返回 -1
        if ((xy + yx) % 2 == 1) {
            return -1;
        }
        
        // 3. 計算最小交換次數
        // xy / 2 代表兩對 xy 換 1 次；yx / 2 代表兩對 yx 換 1 次
        // 若 xy % 2 != 0（代表 xy 和 yx 各剩 1 個），則需額外加 2 次交換
        return xy / 2 + yx / 2 + (xy % 2 == 0 ? 0 : 2);
    }
};
