/**
 * 題目：258. Add Digits (各位相加)
 * 難度：簡單 (Easy)
 * 描述：重複將一個非負整數的各位相加，直到結果為一位數。
 * * 時間複雜度：O(1) - 直接利用數學公式。
 * 空間複雜度：O(1)。
 * * 優化思路：
 * 此題為「數位根」問題，數字 n 的數位根為 n 對 9 的模：
 * 1. 若 n = 0，結果為 0。
 * 2. 若 n % 9 == 0 且 n != 0，結果為 9。
 * 3. 否則，結果為 n % 9。
 * 簡化公式為：(n - 1) % 9 + 1 (適用於 n > 0)。
 */

class Solution {
public:
    int addDigits(int num) {
        // 若輸入為 0，則直接回傳 0
        if (num == 0) return 0;
        
        // 利用數位根公式直接計算，處理 1-9 的情況與 9 的倍數
        return (num - 1) % 9 + 1;
    }
};
